      <h5>Heartland Express 90-Day Review</h5>
       <p>I drive for Knight. I have been here almost 5yrs. They treat me very well and I make a pretty good check each week. They run me in the areas I like to run most of the time. Sometimes I get a load that I'm not crazy about but it can't be want I want all the time. They are not perfect but they have kept me here for this long so they are doing something right. The shops and communication are the biggest problem. I talk to my DM everyday to keep things in line as well as I can. I go home when I want. Rarely do I get home late. Sometimes it can't be avoided. I'm not a recruiter, just a driver that does his job and I don't complain unless I need to. I've been driving since 1988 and this is one of the best companies I've driven for.</p>

